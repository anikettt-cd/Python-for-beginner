class employee():
    salary = 100000. #this is a class attributes
    language = "python"
    
    
harry = employee()
harry.name = "harry poter" #this is an object attributes
print(harry.name,harry.salary,harry.language)

 
rohan = employee()
rohan.name = "rohan das"
print(rohan.name,rohan.salary,rohan.language)

#here name is the object attributes and salary and language are the class attributes as the directly belongs to the class

